def greet(name):
    print(f"Hello {name} Welcome To the paradise")

def farewell(name):
    print(f"Goodbye {name}, see you next time!")

def thank_you(name):
    print(f"Thank you, {name}, for your support!")

def apology(name):
    print(f"Sorry {name}, I didn't mean to upset you.")

def congratulations(name):
    print(f"Congratulations {name} on your achievement!")

def encouragement(name):
    print(f"Keep going {name}, you're doing great!")